import { useState } from "react";
import { PredictionForm } from "@/components/PredictionForm";
import { ResultCard } from "@/components/ResultCard";
import { Card } from "@/components/ui/Card";
import { predictPrice, type PredictionInput, type PredictionResult } from "@/lib/model";
import { cn } from "@/utils/cn";
import { Home, LineChart } from "lucide-react";

export default function App() {
  const [submittedInput, setSubmittedInput] = useState<PredictionInput | null>(null);
  const [result, setResult] = useState<PredictionResult | null>(null);

  const handlePredict = (input: PredictionInput) => {
    const prediction = predictPrice(input);
    setSubmittedInput(input);
    setResult(prediction);
  };

  return (
    <div className="min-h-screen bg-slate-50/50">
      <header className="border-b border-slate-200 bg-white/80 backdrop-blur">
        <div className="mx-auto flex max-w-6xl items-center gap-3 px-6 py-4">
          <div className="flex h-9 w-9 items-center justify-center rounded-lg bg-emerald-600 text-white shadow-sm shadow-emerald-200">
            <Home className="h-5 w-5" aria-hidden="true" />
          </div>
          <div>
            <h1 className="text-lg font-bold tracking-tight text-slate-900">House Price Predictor</h1>
            <p className="hidden text-xs font-medium text-slate-500 sm:block">
              ML-powered property valuation
            </p>
          </div>
          <span className="ml-auto hidden rounded-full bg-emerald-50 px-2.5 py-1 text-xs font-semibold text-emerald-700 sm:inline-block">
            v1.0
          </span>
        </div>
      </header>

      <main className="mx-auto max-w-6xl px-6 py-8 sm:py-12">
        <div className="mb-8 max-w-2xl">
          <h2 className="text-2xl font-bold tracking-tight text-slate-900 sm:text-3xl">
            Estimate your home&apos;s value
          </h2>
          <p className="mt-2 text-slate-600">
            Enter the property details below and our valuation model will generate an instant price estimate with key insights.
          </p>
        </div>

        <div className="grid grid-cols-1 gap-6 lg:grid-cols-2 lg:items-start">
          <PredictionForm onPredict={handlePredict} />

          <div
            aria-live="polite"
            className={cn(
              "transition-all duration-500 ease-out",
              result
                ? "pointer-events-auto translate-y-0 opacity-100"
                : "pointer-events-none translate-y-4 opacity-0 lg:translate-y-0 lg:opacity-100",
            )}
          >
            {result && submittedInput ? (
              <ResultCard result={result} input={submittedInput} />
            ) : (
              <EmptyState />
            )}
          </div>
        </div>
      </main>

      <footer className="border-t border-slate-200 bg-white py-6">
        <div className="mx-auto max-w-6xl px-6 text-center text-xs text-slate-500">
          This estimate is generated by a client-side valuation model for demonstration purposes and should not replace a professional appraisal.
        </div>
      </footer>
    </div>
  );
}

function EmptyState() {
  return (
    <Card className="flex h-full min-h-[420px] flex-col items-center justify-center text-center">
      <div className="mb-4 flex h-14 w-14 items-center justify-center rounded-2xl bg-emerald-50 text-emerald-600">
        <LineChart className="h-7 w-7" aria-hidden="true" />
      </div>
      <h3 className="text-lg font-semibold text-slate-900">Ready to predict</h3>
      <p className="mt-2 max-w-xs text-sm text-slate-500">
        Complete the form and click <span className="font-medium text-emerald-700">Generate price prediction</span> to see your estimate and insights.
      </p>
    </Card>
  );
}
